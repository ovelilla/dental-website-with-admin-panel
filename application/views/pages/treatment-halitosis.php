                <h2>¿Qué es la halitosis?</h2>

                <p>Cuando hablamos de halitosis el principal origen suele ser la boca, aunque no se descarta otros orígenes. Una de las principales causas de la halitosis es el mas estado de las piezas dentales, el cumulo de sarro, la presencia de bolsas periodontales, caries, etc.</p>

