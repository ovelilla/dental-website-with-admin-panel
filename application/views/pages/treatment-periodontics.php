            <div class="col">
                <h2>Factores de riesgo</h2>

                <ul class="standard-list">
                    <li>Mala higiene.</li>
                    <li>Tabaco.</li>
                    <li>Estrés.</li>
                    <li>Enfermedades Generales o defensas bajas: diabetes, osteoporosis, VIH, herpes, transplantados etc.</li>
                    <li>Cambios hormonales: embarazo, menopausia.</li>
                    <li>Antecedentes familiares.</li>
                </ul>

                <p>La PERIODONTITIS tiene mayor incidencia a partir de los 35 años.</p>
