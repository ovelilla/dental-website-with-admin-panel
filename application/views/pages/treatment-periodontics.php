            <h2>¿Que es la periodoncia?</h2>

            <p>La enfermedad periodontal es una infección por bacterias que afecta a los tejidos que dan soporte a los dientes. En su estado más inicial aparece como la denominada GINGIVITIS (inflamación de las encías). Cuando la infección avanza, aparece la destrucción del hueso que sujeta los dientes. Esta infección más avanzada se denomina PERIODONTITIS.</p>

            <blockquote>
                <p><em>En Dentiny disponemos de un equipo de profesionales especializados en el tratamiento y seguimiento de las enfermedades periodontales. No dude en realizar su consulta sin compromiso.</em></p>
            </blockquote>

            <p>Es necesario visitar al dentista para tratar y controlar la enfermedad periodontal. Si la enfermedad periodontal se trata a tiempo se puede evitar la pérdida dental.</p>
