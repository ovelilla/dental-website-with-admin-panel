                <h2>Signos y síntomas de la enfermedad periodontal</h2>

                <ul class="standard-list">
                    <li>Sangrado de las encías.</li>
                    <li>Mal aliento o mal sabor de boca.</li>
                    <li>Encías sensibles.</li>
                    <li>Encías inflamadas.</li>
                    <li>Dolor al masticar.</li>
                    <li>Movilidad dental</li>
                    <li>Hipersensibilidad dental</li>
                    <li>Encías retraídas</li>
                    <li>Espacios entre los dientes que aumentan progresivamente</li>
                </ul>
                </ul>
            </div>
        </div>

        <blockquote>
            <p><em>Si presenta al menos uno de estos signos o síntomas acuda al dentista para una revisión bucal.</em></p>
