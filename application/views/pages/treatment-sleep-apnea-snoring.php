            <h2>¿Cuál es el tratamiento?</h2>

            <p>Lo primero que hay que descartar en estos pacientes es la presencia de pólipos, anginas por parte de un otorrino.</p>

            <p>Una vez descartadas las causas obstructivas, analizaríamos el tipo de mordida y si fuese necesario se le realizaría una férula de avance mandibular que ayudaría a posicionar la mandíbula y a aumentar el espacio de la vía aérea, favoreciendo la respiración y evitando el ronquido y las apneas.</p>

            <p>Todo este diagnóstico tiene que ir de la mano con el medico de cabecera. En casos más graves será necesario estudios del sueño.</p>
