            <h2>¿Qué es la apnea del sueño?</h2>

            <p>La apnea del sueño son episodios en la que el paciente deja de respirar durante unos segundos, suele estar acompañadas de ronquidos y después de varios ciclos de ronquidos el paciente se queda sin aire unos segundos, hasta que vuelve otra vez a respirar.</p>

            <p>Muchos casos de apnea del sueño están relacionados con la retrognatia mandibular; como la mandíbula está muy atrás bloquea la vía aérea produciendo que el flujo de aire disminuya.</p>
        </div>

