                    <h2>¿Debo quitarme las muelas del juicio?</h2>

                    <p>La extracción de las muelas del juicio puede indicarse para prevenir problemas futuros, ya que un cordal mal posicionado retendrá comida y puede ocasionar infecciones; cuando la muela ya está ocasionando una patología, bien sea caries en las muelas de alrededor, inflamación o dolor; o cuando se necesita espacio para reposicionar los dientes con ortodoncia. Si no existe ningún problema y su crecimiento es óptimo no está indicada su extracción.</p>
