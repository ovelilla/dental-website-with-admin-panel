                    <h2>Extracciones complejas</h2>

                    <p>En algunas ocasiones, las extracciones de las piezas dentales pueden resultar complicadas por su posición o por su integridad (dientes rotos). También recurrimos a la cirugía cuando las piezas dentales se encuentran retenidas en el hueso.</p>

                    <p>Las muelas del juicio o cordales son las últimas muelas en salir, y la mayoría de las veces no tienen espacio para hacerlo quedando retenidas en el hueso total o parcialmente, o si lo hacen, la encía de alrededor se inflama debido a la dificultad para limpiar esa zona provocando dolor e inflamación.</p>
                </div>
            </div>

            <p>Los caninos (colmillos) en gran cantidad de veces se quedan retenidos en el hueso porque no pueden erupcionar por si solos por la falta de espacio. Es entonces cuando realizamos una fenestración del diente, quitamos el hueso y el tejido de alrededor para que quede libre y pueda salir. Este procedimiento se emplea en los tratamientos de ortodoncia.</p>
        </div>

        <div class="col">
            <h2>Cirugía de la encía</h2>

            <p>La retracción de las encías o recesión gingival es muy común en la población. Las encías se desplazan dejando a la vista la raíz del diente. Algunas de las causas de esta retracción pueden ser ocasionadas por un cepillado agresivo con un cepillo de cerdas muy duras, por apretar los dientes a causa del bruxismo, por enfermedad periodontal o por mala higiene.</p>

            <div class="row">

                <div class="col">
                    <p>Para recuperar esa encía perdida se realizan injertos de encía, que consiste en hacer una pequeña incisión en la encía del paladar e implantarla en la zona deseada, devolviendo así la estética y la salud gingival.</p>

                    <p>Por otra parte, también encontramos casos en los que hay un sobrecrecimiento de las encías y hace que los dientes se vean más pequeños. Es entonces cuando realizamos una gingivectomía, unos pequeños cortes en los que eliminamos esa encía sobrante.</p>
