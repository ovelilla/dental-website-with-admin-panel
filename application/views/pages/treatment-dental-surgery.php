            <h2>¿Tienes dolor de muelas?</h2>

            <p>Las muelas del juicio retenidas pueden provocar dolor y otros problemas dentales. Debido a que son difíciles de limpiar pueden ser más vulnerables a las caries dentales. La temprana detección y el diagnostico pueden corregir el problema rápido, acude al dentista con regularidad antes de que empeore tu salud bucodental.</p>

