            <h2>¿Qué es la oclusión?</h2>

            <p>La oclusión es la relación normal de las muelas y los dientes cuando los maxilares están cerrados.</p>

            <p>Una oclusión normal es una boca en la que todos los dientes están presentes y ocluyen de un modo saludable, estable y satisfactorio, aunque presenten pequeñas variaciones en su posición, “dentro de ciertos límites”.</p>

            <p>Por lo tanto, el diagnóstico ortodóncico se basa principalmente en el concepto de una posición ideal, que es muy importante conocer antes de empezar cualquier tratamiento.</p>

            <p>Para realizar un correcto diagnóstico necesitaremos radiografías del paciente, laterales, panorámicas y en algunos casos como caninos o otros dientes incluidos también podemos solicitar radiografías en 3D, que también pueden ser útiles para ver la calidad de hueso de la tabla vestibular en algunos tratamientos en donde la protrusión va a ser inevitable.</p>

            <p>Además, haremos unos moldes y unas fotografías profesionales para tener un registro completo del paciente y así realizar un diagnóstico correcto.</p>

            <p>Posteriormente determinamos si el problema es dentario o si realmente hay un problema óseo, en donde si el paciente es joven podremos actuar a través de la ortopedia y en pacientes adultos podemos realizar pequeños cambios ortopédicos ayudados de micro implantes, muy útiles hoy en día en el campo de la ortodoncia.</p>

