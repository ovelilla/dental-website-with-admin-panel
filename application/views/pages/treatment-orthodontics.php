            <p>Debemos recordar que la ortodoncia nació históricamente por una exigencia social. El deseo de los seres humanos de tener unos dientes bonitos y bien alineados que diesen belleza a su sonrisa. Los tratamientos realizados parten del siglo dieciocho y diecinueve, pero nunca fueron presentados por sus autores como un procedimiento terapéutico que sanase una enfermedad.</p>

            <p>Aunque a principios del siglo XX empezó a darse a la ortodoncia consideraciones especiales de que el tratamiento no solo era estético, sino que también al mejorar la posición dentaria la función se veía muy mejorada.</p>

